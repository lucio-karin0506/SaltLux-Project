package bicyclelogic;

public class BicycleVO {
	private String lental_count;
	private String lental_office;
	private String return_count;
	private String return_office;
	
	public BicycleVO() {
		super();
	}

	public String getLental_count() {
		return lental_count;
	}

	public void setLental_count(String lental_count) {
		this.lental_count = lental_count;
	}

	public String getLental_office() {
		return lental_office;
	}

	public void setLental_office(String lental_office) {
		this.lental_office = lental_office;
	}

	public String getReturn_count() {
		return return_count;
	}

	public void setReturn_count(String return_count) {
		this.return_count = return_count;
	}

	public String getReturn_office() {
		return return_office;
	}

	public void setReturn_office(String return_office) {
		this.return_office = return_office;
	}
	
}
