package bicyclelogic;

public class LocationVO {
	private int rentalNo;
	private String rentalOfficeName;
	private int rentalTotalCnt;
	
	public LocationVO() {
		super();
	}
	
	public int getRentalNo() {
		return rentalNo;
	}
	
	public void setRentalNo(int rentalNo) {
		this.rentalNo = rentalNo;
	}
	
	public String getRentalOfficeName() {
		return rentalOfficeName;
	}
	
	public void setRentalOfficeName(String rentalOfficeName) {
		this.rentalOfficeName = rentalOfficeName;
	}
	
	public int getRentalTotalCnt() {
		return rentalTotalCnt;
	}
	
	public void setRentalTotalCnt(int rentalTotalCnt) {
		this.rentalTotalCnt = rentalTotalCnt;
	}

}
