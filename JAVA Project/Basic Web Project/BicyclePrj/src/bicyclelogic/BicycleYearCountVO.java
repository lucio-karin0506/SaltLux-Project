package bicyclelogic;

public class BicycleYearCountVO {
	private String month;
	private String month_2019;
	private String month_2020;
	private Double increasing_pct;
	
	public BicycleYearCountVO() {
		super();
	}

	public String getMonth() {
		return month;
	}

	public void setMonth(String month) {
		this.month = month;
	}

	public String getMonth_2019() {
		return month_2019;
	}

	public void setMonth_2019(String month_2019) {
		this.month_2019 = month_2019;
	}

	public String getMonth_2020() {
		return month_2020;
	}

	public void setMonth_2020(String month_2020) {
		this.month_2020 = month_2020;
	}

	public Double getIncreasing_pct() {
		return increasing_pct;
	}

	public void setIncreasing_pct(Double increasing_pct) {
		this.increasing_pct = increasing_pct;
	}
	
	
}
